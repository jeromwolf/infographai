// Helper function to get templates by category
export function getTemplatesByCategory(category: string): PresetTemplate[] {
  return presetTemplates.filter(template => template.category === category);
}

// Get all unique categories
export function getCategories(): string[] {
  return [...new Set(presetTemplates.map(template => template.category))];
}

// Get template by name
export function getTemplateByName(name: string): PresetTemplate | undefined {
  return presetTemplates.find(template => template.name === name);
}

// Search templates
export function searchTemplates(query: string): PresetTemplate[] {
  const lowercaseQuery = query.toLowerCase();
  return presetTemplates.filter(template =>
    template.name.toLowerCase().includes(lowercaseQuery) ||
    template.category.toLowerCase().includes(lowercaseQuery) ||
    template.description?.toLowerCase().includes(lowercaseQuery)
  );
}